        <p class="text-dark text-justify font-arial mt-3"> Gereja Katolik Santo Fransiskus Xaverius Mempawah adalah sebuah gereja paroki yang terletak di pusat kota Kabupaten Mempawah, Kalimantan Barat. Gereja ini merupakan salah satu Gereja Katolik yang berada di bawah naungan Dekanat Landak, Keuskupan Agung Pontianak. Gereja ini berlokasi di Jalan Teratai No. 1 Mempawah, dan menjadi pusat rohani bagi komunitas Katolik setempat.
Saat ini gereja paroki memiliki 18 jumlah stasi yang ada di dua kecamatan yaitu Kecamatan Sungai Kunyit dan Kecamatan Sadaniang. Kecamatan Sungai Kunyit hanya memiliki 1 gereja stasi saja yang ada di daerah Bangkam, sisanya gereja stasi berada di Kecamatan Sadaniang. Gereja Paroki memiliki 2 Pastor yang ditugaskan untuk melayani pada saat ini ialah RD Anton Silvinus dan RD Alex Mardalis.
Paroki Mempawah berdiri pada tanggal 3 Desember 2020 yang diresmikan oleh Bapak Uskup Agung Pontianak Mgr. Agustinus Agus. Paroki ini merupakan pemekaran dari Paroki St. Christhoporus Sungai Pinyuh. Gereja Stasi Mempawah dulunya diberi pelindung St. Petrus. Gereja stasi St, Petrus Mempawah ini melayani para pegawai-pegawai yang menetap untuk bekerja di Kabupaten Pontianak (saat itu), namun setelah pemekaran, Santo pelindung diganti menjadi St. Fransiskus Xaverius. Mengenai orang-orang katolik di Mempawah sendiri sudah disebutkan sejak tahun 1917 di daerah Sangking Mempawah. Namun tidak disebutkan bahwa kapan Gereja di Kota Mempawah berdiri.
Stasi St. Petrus Mempawah diangkat statusnya menjadi Kwasi Paroki pada tanggal 4 April 2019 oleh Mgr. Agustinus Agus dan ditugaskan seorang imam yaitu Pastor Anton Silvinus untuk bertugas di sana kemudian dibantu oleh Diakon Peran. Bangunan yang sekarang digunakan sebagai pastoran merupakan rumah yang dibeli oleh keuskupan dari Bapak Cornelis, MH, yang menjadi tempat berlangsungnya berbagai kegiatan pastoral dan administratif dalam mendukung kehidupan beriman di Paroki Santo Fransiskus Xaverius Mempawah.
